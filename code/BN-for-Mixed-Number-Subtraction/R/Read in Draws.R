#########################################################################################################################
# call the 'R2WinBUGS' library
#########################################################################################################################
library(R2WinBUGS)


#########################################################################################################################
# Define the R object with the names of the coda files
#########################################################################################################################
coda.file.names <- bugs.sim


#########################################################################################################################
# Read the files into coda
#########################################################################################################################
draws.from.bugs <- read.bugs(coda.file.names)


#########################################################################################################################
# Convert the draws to a matrix
#########################################################################################################################
draws.from.bugs.as.matrix <- as.matrix(draws.from.bugs)

colnames(draws.from.bugs.as.matrix)

